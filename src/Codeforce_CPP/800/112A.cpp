#include <bits/stdc++.h>

/* Маленький Петя очень любит подарки. Его мама подарила ему на день рождения две строки равной длины, состоящие из больших
и маленьких букв латинского алфавита. Теперь Петя хочет сравнить эти строки лексикографически. При этом регистр букв значения
не имеет, то есть большая буква считается эквивалентной соответствующей маленькой букве. Помогите Пете выполнить сравнение.

Входные данные
В каждой из первых двух строк записано по одной подаренной строке. Длина строк находится в пределах от 1 до 100 включительно.
Гарантируется, что строки имеют одинаковую длину, а также состоят из больших и маленьких букв латинского алфавита.

Выходные данные
Если первая строка меньше второй, выведите «-1». Если вторая строка меньше первой, выведите «1». Если строки равны, выведите «0».
Учтите, что регистр букв не учитывается при сравнении.*/

int main()
{
	std::string str1 = " ", str2 = " ";
	std::cin >> str1 >> str2;
	std::string str1Lower(str1.size(), ' ');
    std::string str2Lower(str2.size(), ' ');

	std::transform(str1.begin(), str1.end(), str1Lower.begin(), ::tolower);
	std::transform(str2.begin(), str2.end(), str2Lower.begin(), ::tolower);

	int res = 0;
	for (int i = 0; i < str1.size(); i++)
	{
		if (str1Lower[i] < str2Lower[i])
		{
			res--;
			break;
		}
		else if (str1Lower[i] > str2Lower[i])
		{
			res++;
			break;
		}
	}

	std::cout << res << std::endl;
}